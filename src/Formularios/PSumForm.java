    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double a = Double.valueOf(this.txtEa.getText());
        double b = Double.valueOf(this.txtEb.getText());
        double p = Double.valueOf(this.txtPosibles.getText());
        double ab = Double.valueOf(this.txtAyB.getText());
        
        PSumClass pSumClass = new PSumClass(a, b, p, ab);
        
        String Pa = String.valueOf(pSumClass.getA() / pSumClass.getP());
        this.txtPa.setText(Pa);
        
        String Pb = String.valueOf(pSumClass.getB() / pSumClass.getP());
        this.txtPb.setText(Pb);
        
        this.txtAoB.setText(String.valueOf(pSumClass.calcular()));
    }//GEN-LAST:event_jButton1ActionPerformed

